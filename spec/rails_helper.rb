# Some versions of Rails' ActiveRecord::TestFixtures don't expose the class-level
# fixture helpers that rspec-rails expects (fixture_path=, use_transactional_tests=,
# use_instantiated_fixtures=). On those setups, requiring rspec/rails will try to
# call those setters on example groups and raise NoMethodError. Provide a tiny
# shim so rspec-rails can safely use fixture support.
unless defined?(ActiveRecord::TestFixtures) && ActiveRecord::TestFixtures.respond_to?(:included)
  # nothing to do if TestFixtures isn't present
else
  module ActiveRecord
    module TestFixtures
      module ClassMethods
        def fixture_path=(path)
          @fixture_path = path
        end

        def use_transactional_tests=(val)
          @use_transactional_tests = val
        end

        def use_instantiated_fixtures=(val)
          @use_instantiated_fixtures = val
        end

        def fixtures(*_args)
          # no-op shim for rspec-rails when actual fixture loading isn't used
        end
      end

      def self.included(base)
        base.extend ClassMethods
      end
    end
  end
end

